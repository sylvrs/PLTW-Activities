def add_tip(total, tip_percent):
	'''Return the total amount of a meal including tip'''
	return total + (tip_percent * total)

def hyp(leg1, leg2):
	return ((leg1 ** 2) + (leg2 ** 2)) ** 0.5

def mean(a, b, c):
	return (a + b + c) / 3

def perimeter(base, height):
	return (base * 2) + (height * 2)

